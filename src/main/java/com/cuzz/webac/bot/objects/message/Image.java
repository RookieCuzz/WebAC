package com.cuzz.webac.bot.objects.message;

public class Image {

    protected long size;

    protected String filename;

    protected String file;

    protected String url;

    public String getFilename() {
        return filename;
    }

    public String getUrl() {
        return url;
    }

    public long getSize() {
        return size;
    }

    public String getFile() {
        return file;
    }
}
